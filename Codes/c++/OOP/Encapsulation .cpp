#include<bits/stdc++.h>
using namespace std;

class Student {
public:
    int x;
    void sayHi(){
        cout << "Hi " << x;
    }
};

int main(){
    Student Rakib;
    Rakib.x = 03;
    Rakib.sayHi();
 
}

