#include<bits/stdc++.h>   
using namespace std;   

template<typename T> 
T sum(T a,T b){
    T s = a+b;
    return s;
}

int main(){   
    int a=2,b=4;
    cout << sum<int> (a,b) << '\n';
    
    double x=2.5,y=3.4;
    cout << sum<double>(x,y) << '\n';
    
    return 0;   
}   
   
   
